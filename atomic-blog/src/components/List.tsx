import React from "react";
import { PostType } from "../types";

export default function List({ posts }: { posts: PostType[] }) {
  return (
    <ul>
      {posts.map((post, i) => (
        <li key={i}>
          <h3>{post.title}</h3>
          <p>{post.body}</p>
        </li>
      ))}
    </ul>
  );
}
